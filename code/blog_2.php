    <h2 class="w3-center w3-padding-64 "><span class="w3-tag w3-wide ">Do You Know The Symptoms Of Jaundice?</span></h2>
    <p>Jaundice is a condition which involves yellowish discoloration of the skin, the eyes, and the mucous. Many refer to jaundice as a disease, however, it is actually a condition which points to an underlying disease. The yellow discoloration of the skin during jaundice occurs because of increased amounts of destroyed red blood cells. These destroyed cells go by the name Bilirubin. While infants are more likely to catch jaundice, people of all ages are also vulnerable to this condition. Thus, one should always be on the lookout for jaundice symptoms. As is the case with most diseases, seeking medical attention upon detecting symptoms of jaundice can help in early medical diagnosis.</p>
<div class="w3-panel w3-leftbar w3-light-grey">

<ol class="w3-border w3-black">Causes of Jaundice </ol>
<p><i>
One of the most common causes of jaundice and jaundice symptoms is a problem with the liver. It is liver’s duty to filter out the waste from the blood and then circulate it through the body. However, if the liver fails to filter out the waste from the blood, then it may lead to the circulation of bilirubin in the bloodstream. This ultimately causes yellow hue throughout the body.</i></p>
<ol class="w3-border w3-black">Symptoms of Jaundice</ol>
<p><i>
The most common symptoms of Jaundice involve:</i></p>
<ol class="w3-border w3-black">1. Yellowing of Skin</ol>
<p><i>As explained earlier, it is the bilirubin which causes the yellowing of the skin or the mucous. This yellow discoloration is one of the most common jaundice symptoms.</i></p>
<ol class="w3-border w3-black">2. Pale Stools</ol>
<p><i>
The presence of pale stools indicates problems with the liver. Bile, a by-product of liver gives stool its normal color. While one of the jaundice symptoms, pale stool can also indicate other problems.</i></p>
<ol class="w3-border w3-black">3. Dark Urine</ol>
<p><i>Dark urine is again indicative of a problem with liver and bile production. Hence dark urine also classifies as one of the jaundice symptoms.</i></p>
<ol class="w3-border w3-black">4. Itchiness</ol>
<p><i>
Itchiness, while being one of the jaundice symptoms, should be accompanied by one of the other symptoms of jaundice. When the damaged red blood cells start accumulating in the bloodstream, it causes itchiness.</i></p>
<ol class="w3-border w3-black">5. Fatigue</ol>
<p><i>
One’s state of physical and mental exhaustion is what many refer to as fatigue. While fatigue could be the result of exhaustion by doing a tiring physical activity, it is also one of the common jaundice symptoms.</i></p>
    <img src="../pic/3.jpg"style="width:100%;max-width:1000px" class="w3-margin-top"><ol class="w3-border w3-black">6. Abdominal Pain</ol>
<p><i>
In case of problems with the life arising due to jaundice, abdominal pain is the commonly occurring jaundice symptoms. The pain in the abdomen could feel like a cramp or a stabbing pain.</i></p>

<ol class="w3-border w3-black">7. Extreme Weight Loss</ol>
<p><i>
In such a case where jaundice does not arise due to infection, extreme weight loss is not only one of the jaundice symptoms but also a symptom of liver problems.</i></p>

<ol class="w3-border w3-black">8. Nausea Or Vomiting</ol>
<p><i>
If a person constantly feels nauseous, then it could indicate that the person is suffering from jaundice. As jaundice symptoms go, nausea or vomiting cause the most discomfort. Many people confuse this with improper digestion and take medications accordingly. However, this is not recommended, instead, one should immediately consult a doctor.</i></p>
<ol class="w3-border w3-black">9. Diarrhoea</ol>
<p><i>
Diarrhoea is the bowel distress resulting in three to four liquid bowel movements in a day. While a condition itself, diarrhoea is also one of the common jaundice symptoms. Diarrhoea has a tendency to last up to a week. It also leads to severe dehydration of the body.</i></p>
<ol class="w3-border w3-black">10. Fever</ol>
<p><i>
Fever is yet again one of the common jaundice symptoms. If a patient is diagnosed with both a fever and jaundice then the likely underlying disease or condition is hepatitis B.</i></p></div>
<div class="w3-panel w3-leftbar w3-light-grey">

<ol class="w3-border w3-black">
Treatment For Jaundice & Jaundice Symptoms</ol>
<p><i>
Usually after the jaundice symptoms surface and the doctor has diagnosed doctor would recommend any counteracting medicine. However, in rare cases, doctors may even suggest surgical treatment.</i></p></div>

<div class="w3-panel w3-leftbar w3-light-grey">

<ol class="w3-border w3-black">Take Away</ol>
<p><i>
Jaundice can cause a lot of discomforts and if not treated in time, can lead to other severe problems. Thus, it is necessary to get the right medical attention once the patient in concern shows jaundice symptoms.</i><br>
